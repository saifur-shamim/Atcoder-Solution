const int mx=1e5+24;

vector<int> adj[mx];
vector<int> in_degree;

int ans;
int dis[mx];

void TopoSort(int n)
{

    queue<int> q;
    for(int i=1; i<=n; i++)
    {
        if(in_degree[i]==0)
        {
            q.push(i);
        }
    }
    while(!q.empty())
    {
        int curr=q.front();
        q.pop();

        for(auto it:adj[curr])
        {
            dis[it]=dis[curr]+1;
            ans=max(ans,dis[it]);

            in_degree[it]--;
            if(in_degree[it]==0)
            {

                q.push(it);
            }
        }
    }

}
