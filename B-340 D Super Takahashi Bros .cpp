void dijkstra(ll s,ll n)
{
    for(ll i=0; i<=n; i++)
    {
        dis[i]=infll;
    }

    dis[s]=0;

    priority_queue<pll,vpl,greater<pll>> pq;

    pq.push({0,s});

    while(!pq.empty())
    {
        ll curd=pq.top().F;
        ll node=pq.top().S;
        pq.pop();
        if(dis[node]<curd) continue;
        for(auto it:adj[node])
        {
            ll child=it.F;

            ll w=it.S;

            if(curd+w<dis[child])
            {
                dis[child]=curd+w;
                pq.push({dis[child],child});
            }
        }
    }
}
